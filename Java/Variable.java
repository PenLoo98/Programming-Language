public class Variable{
    public static void main(String[] args){
        int count=1;
        System.out.println("count == "+count);
        count++;
        System.out.println("count+1 == "+count);
        
        double stack=1.5;
        System.out.println("stack is "+stack);
        stack+=1.7;
        System.out.println("stack+1.7 is "+stack);
    }};
